package org.example.animals;

/**
 * Represents an abstract class for animals.
 */
public abstract class Animal {

    /**
     * Defines the behavior of eating for the animal.
     */
    public abstract void eat();

    /**
     * Defines the behavior of moving for the animal.
     */
    public abstract void move();

    /**
     * Defines the behavior of reproduction for the animal.
     */
    public abstract void reproduce();

    /**
     * Defines the behavior of death for the animal.
     */
    public abstract void die();
}
